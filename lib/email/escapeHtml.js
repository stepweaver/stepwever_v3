/**
 * Escape text for interpolation into HTML email bodies (not full HTML sanitizer).
 */
export function escapeHtmlForEmail(value) {
  if (value == null) return '';
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
